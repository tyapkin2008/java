public class Main {
    public static void main(String[] args) {
        System.out.println("Максимальное число int " + Integer.MAX_VALUE);
        System.out.println("Минимальное число int " + Integer.MIN_VALUE);

        System.out.println("Максимальное число long" + Long.MAX_VALUE);
        System.out.println("Минимальное число long" + Long.MIN_VALUE);

        System.out.println("Максимальное число short" + Short.MAX_VALUE);
        System.out.println("Минимальное число short" + Short.MIN_VALUE);

        System.out.println("Максимальное число float " + Float.MAX_VALUE);
        System.out.println("Минимальное число float " + -Float.MAX_VALUE);

        System.out.println("Максимальное число double " + Double.MAX_VALUE);
        System.out.println("Минимальное число double " + -Double.MAX_VALUE);


    }
}
